    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
        // Get the ID from the ID field
        int id = Integer.parseInt(jTextField1.getText().trim());

        // Connect to the database
        Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/gestionstock", "root", "root");

        // Create a PreparedStatement object to execute the SQL query
        String sql = "SELECT * FROM produitfrais WHERE id=?";
        PreparedStatement statement = connection.prepareStatement(sql);
        statement.setInt(1, id);

        // Execute the query and get the results
        ResultSet resultSet = statement.executeQuery();

        DefaultTableModel model = new DefaultTableModel(new String[]{"Id", "Nom", "Quantité" , "Date Perrmation" ," Temperature"}, 0);

        // Loop through the results and add them to the model
        while (resultSet.next()) {
            String nom = resultSet.getString("nom");
            int quantite = resultSet.getInt("qte");
            Date datePeremption =  resultSet.getDate("datePeremption");
            int temperature = resultSet.getInt("temperature") ;
            model.addRow(new Object[]{id, nom, quantite,datePeremption,temperature});
        }

        // Set the model for your JTable component
        jTable.setModel(model);

        // Close the connection, statement, and result set
        resultSet.close();
        statement.close();
        connection.close();
    } catch (NumberFormatException ex) {
        System.out.println("Error: Invalid ID");
    } catch (SQLException ex) {
        System.out.println("Error: " + ex.getMessage());
    }
    }//GEN-LAST:event_jButton2ActionPerformed

