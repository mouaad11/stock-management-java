    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
Connection conn = null;
    PreparedStatement stmt = null;
    ResultSet rs = null;
    try {
        // Connect to the database
        conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/gestionstock", "root", "root");

        // Prepare the SQL statement
        String sql = "SELECT * FROM produitfrais";
        stmt = conn.prepareStatement(sql);

        // Execute the query and get the result set
        rs = stmt.executeQuery();

        // Populate the table with the results
        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        model.setRowCount(0); // Clear the table
        while (rs.next()) {
            int id = rs.getInt("id");
            String nom = rs.getString("nom");
            int quantite = rs.getInt("qte");
            Date datePeremption =  rs.getDate("datePeremption");
            int temperature = rs.getInt("temperature") ;
            model.addRow(new Object[]{id, nom, quantite,datePeremption,temperature});
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
    } finally {
        // Close the resources
        try {
            if (rs != null) rs.close();
            if (stmt != null) stmt.close();
            if (conn != null) conn.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    try {
        // Connect to the database
        Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/gestionstock", "root", "root");

        // Get the ID from the ID text field
        int id = Integer.parseInt(jTextField1.getText());

        // Create a PreparedStatement object to execute the SQL query
        String sql = "DELETE FROM produitfrais WHERE id = ?";
        PreparedStatement statement = connection.prepareStatement(sql);
        statement.setInt(1, id);

        // Execute the query and get the number of rows affected
        int rowsDeleted = statement.executeUpdate();

        // If a row was deleted, show a message and update the table in the UI
        if (rowsDeleted > 0) {
            JOptionPane.showMessageDialog(this, "Row deleted successfully.");
        } else {
            JOptionPane.showMessageDialog(this, "No row found with ID " + id + ".");
        }

        // Close the connection and statement
        statement.close();
        connection.close();
    } catch (SQLException ex) {
        System.out.println("Error: " + ex.getMessage());
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Please enter a valid ID.");
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

